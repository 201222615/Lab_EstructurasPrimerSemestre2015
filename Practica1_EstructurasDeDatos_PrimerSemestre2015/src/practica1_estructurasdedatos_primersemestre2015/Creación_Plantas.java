    private void fileImagenPlantasActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_fileImagenPlantasActionPerformed
        // TODO add your handling code here:
          JFileChooser seleccionar = (JFileChooser)evt.getSource();
        String command = evt.getActionCommand();
        if (command.equals(JFileChooser.APPROVE_SELECTION)){
            File archivo = seleccionar.getSelectedFile();
            image=archivo.getAbsolutePath();
            cargarimagen(image);
            DialogoBuscador.setVisible(false);
        }else if (command.equals(JFileChooser.CANCEL_SELECTION)){
            cargarimagen("");
            DialogoBuscador.setVisible(false);
        }
    }//GEN-LAST:event_fileImagenPlantasActionPerformed

