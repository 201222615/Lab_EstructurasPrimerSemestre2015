    public void ResetearoRecuperarInformacion(){
        //limpiamos todos los combos
        this.comboPuntos.removeAllItems();
        this.comboDefensa.removeAllItems();
        this.comboTipoAtaque.removeAllItems();
        this.txtNombrePlanta.setText("");
                
        //llenamos los combo de puntos de ataque y defensa
        for (int i=1;i<=1000000;i++){
            this.comboPuntos.addItem(i);
            this.comboDefensa.addItem(i);
        }
  
        //Ataques Disponibles
        this.comboTipoAtaque.addItem("Disparo");
        this.comboTipoAtaque.addItem("Golpe");
        
        //limpiamos memoria
        cargarimagen("");
        image="";
    }

